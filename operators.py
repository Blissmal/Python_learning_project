
num1 = 120
num2 = 50

# arithmetic operators
print(num1 + num2)
print(num1 - num2)
print(num1 * num2)
print(num1 / num2)
print(num1 % num2)
print(num1 ** num2)

# comparison operator
x = 10
y = 15
z = "10"
print(x == y)
print(x != y)
print(x < y)
print(x > y)
print(x <= y)
print(x >= y)
print(x == z)

# logical operator
print(x > 3 and x == 10)
print(x < 3 and x == 10)
print(x < 3 or x == 10)
