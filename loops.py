# for loop
fruits = ["Apples", "Bananas", "Oranges", "Mangoes"]
my_number = (3, 2, 7, 8, 9, 17)

for num in range(5, 15):
    print(num)

for Num in range(2, 9):
    print(Num)

for Fruits in fruits:
    print(Fruits)

for Numbers in my_number:
    print(Numbers)

#  while loop
num1 = 5
while num1 <= 15:
    print(num1)
    num1 = num1 + 1
